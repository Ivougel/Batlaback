function buildItemCatalog() {
  const out = {};
  Object.entries(ITEM_CATALOG_RAW).forEach(([key, opts]) => {
    if (opts._isContainerEntry) {
      const { _isContainerEntry, ...container } = opts;
      out[key] = container;
      return;
    }
    out[key] = defItem(opts);
  });
  return out;
}

