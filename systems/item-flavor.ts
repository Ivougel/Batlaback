const TAG_PRIORITY = [
  "weapon",
  "magic",
  "poison",
  "cold",
  "fire",
  "armor",
  "food",
  "pet",
  "debuff",
  "economy",
  "craft",
  "container",
  "melee",
  "utility",
  "accessory",
] as const;

function hashId(id: string): number {
  let h = 0;
  for (let i = 0; i < id.length; i += 1) {
    h = (h * 31 + id.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

function pickTemplate(templates: readonly string[], seed: string): string {
  if (!templates.length) return GRIM_BY_TAG.default[0];
  return templates[hashId(seed) % templates.length];
}

function resolveGrimTag(def: CatalogDef | null | undefined): string {
  const tags = def?.tags ?? [];
  for (const tag of TAG_PRIORITY) {
    if (tags.includes(tag)) return tag;
  }
  if (def && "isContainer" in def && (def as { isContainer?: boolean }).isContainer) return "container";
  return "default";
}

function getItemGrimFlavor(itemId: string): string {
  if (ITEM_GRIM_FLAVOR[itemId]) return ITEM_GRIM_FLAVOR[itemId];

  const catalog = typeof ITEM_CATALOG !== "undefined" ? ITEM_CATALOG : {};
  const def = catalog[itemId] as CatalogDef | undefined;
  const tag = resolveGrimTag(def);
  const pool = GRIM_BY_TAG[tag] ?? GRIM_BY_TAG.default;
  const name = def?.name || itemId;
  const base = pickTemplate(pool, `${itemId}:${tag}`);

  if (def?.classRestriction && hashId(itemId) % 3 === 0) {
    return `${base} Оставлено для тех, кто ещё помнит своё имя — ${name.toLowerCase()} не прощает забывчивых.`;
  }

  return base;
}

